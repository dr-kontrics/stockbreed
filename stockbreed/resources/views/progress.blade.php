            <h4>>>>What Does The Production Process of Broilers Look Like?</h4>
            <h6></h6>
            <p>
                The production process of broilers starts with the breeding organisations, they breed and develop different broiler breeds.
                The breeding organisations supply one-day-old chicks to the rearing companies, who raise these chicks to adult parent stock,
                which are transported to the breeder farms. The breeders produce hatching eggs for the hatcheries, which hatch them to produce
                chicks for the broiler farms.
            </p>
            <h4>>>>How do you start with broilers?</h4>
            <p>
                How to set up a broiler production column depends on the intended production volume and the local market conditions and needs.
                If the focus is on the fresh market, you will need to set up the column differently from when you want to produce frozen chickens.

                When setting up a new production column, the starting point is the number of chicks per carcass weight that need to be slaughtered each week.
                This determines the capacity of the slaughterhouse and from there you can calculate the number of locations, the number of houses per location and the number of broilers per house. On this, the hatchery capacity, the required hatching egg production and the broiler breeder facilities need to be based. There are calculation models available for this, which can be calculated together with investors.
            </p>
            <h4>>>>What production systems are available for broilers?</h4>
            <h6>There are three systems to hatch chicks:</h6>
            <p>
            >Traditional: Hatching eggs stay in the incubator for 18 days, after that the fertilized incubated eggs
                 go to the hatcher. After day 21, the chicks are collected and sorted, after which they are transported to the broiler house.
            </p>
            <p>
            >Hatcheries can also provide water and feed to the newborn chicks in the hatchery,
                 which allows the chicks to develop better. Further processing of the chicks is done in the traditional way.
            </p>
            <p>
            >On-farm hatching (like in our X-Treck and Patio): after 18 days of incubating, the eggs are transported to the house and the chicks hatch in the house,
                 they get water and feed directly and do not need to be transported from the hatchery to the house.
            </p>
            <h4>>>>When is the production of broilers sustainable?</h4>
            <p>
                In a sustainability analysis, economic, ecological and social aspects are further divided into themes and/or
                measurable indicators that are important for sustainability.
                The economic aspects of sustainability are mainly determined by the profitability of the broiler farm;
                not only farm management is important but also the quality of the day-old chicks is essential
                for good performance and to increase profitability.
            </p>
